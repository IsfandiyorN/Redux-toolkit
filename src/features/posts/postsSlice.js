const postsSlice = createSlice({
  name: 'posts',
  initialState,
  reducers: {
    addPost: (state, action) => {
      state.posts.push(action.payload);
    },
    deletePost: (state, action) => {
      state.posts = state.posts.filter(post => post.id !== action.payload);
    },
    setSearch: (state, action) => {
      state.search = action.payload;
    },
    setPostTitle: (state, action) => {
      state.postTitle = action.payload;
    },
    setPostBody: (state, action) => {
      state.postBody = action.payload;
    },
    setSearchResults: (state, action) => {
      state.searchResults = action.payload;
    }
  }
});

export const { addPost, deletePost, setSearch, setPostTitle, setPostBody, setSearchResults } = postsSlice.actions;

export default postsSlice.reducer;
